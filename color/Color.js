export function Hex(hex) {
  hex.replace(/^#/, "");
  var bigInt = parseInt(hex, 16);
  var r = (bigInt >> 16) & 255;
  var g = (bigInt >> 8) & 255;
  var b = bigInt & 255;
  return Color.new(r, g, b);
}
export function rgb(r, g, b) {
  return Color.new(r, g, b);
}

